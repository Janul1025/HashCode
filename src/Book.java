public class Book {
    public int bookId;
    public int score;

    public Book(int bookId, int score) {
        this.bookId = bookId;
        this.score = score;
    }

    public int getBookId() {
        return bookId;
    }

    public void setBookId(int bookId) {
        this.bookId = bookId;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }
}

